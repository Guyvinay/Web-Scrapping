class Restaurant :
    def __init__(self, id, restaurant_name, restaurant_cuisine, restaurant_rating, distance_from_location, delivery_time, promotional_offers, restaurant_image) :
        self.id = id
        self.restaurant_name = restaurant_name
        self.restaurant_cuisine = restaurant_cuisine
        self.restaurant_rating = restaurant_rating
        self.distance_from_location = distance_from_location
        self.delivery_time = delivery_time
        self.promotional_offers = promotional_offers
        self.restaurant_image = restaurant_image